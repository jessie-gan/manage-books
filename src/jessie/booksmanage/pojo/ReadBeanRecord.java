package jessie.booksmanage.pojo;

public class ReadBeanRecord {
	private Integer id;
	private String stuNum;
	private String optionTime;
	private Integer option;
	private Integer readBean;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getStuNum() {
		return stuNum;
	}
	public void setStuNum(String stuNum) {
		this.stuNum = stuNum;
	}
	public String getOptionTime() {
		return optionTime;
	}
	public void setOptionTime(String optionTime) {
		this.optionTime = optionTime;
	}
	public Integer getOption() {
		return option;
	}
	public void setOption(Integer option) {
		this.option = option;
	}
	public Integer getReadBean() {
		return readBean;
	}
	public void setReadBean(Integer readBean) {
		this.readBean = readBean;
	}
	
}
